package com.edgedo.bigdata.queryvo;

/**
 * 分组统计实体
 */
public class CarGroupCount {
    //车辆类型
    private String carType;
    //车辆个数
    private int carCount;
    //核载质量
    private double totalTakeMass;
    //准牵引质量
    private double totalPullMass;
    //核载人数
    private double sitPeopleNum;
    //总时长
    private double timeTotal;
    //总里程
    private double mileageTotal;

    public double getTimeTotal() {
        return timeTotal;
    }

    public void setTimeTotal(double timeTotal) {
        this.timeTotal = timeTotal;
    }

    public double getMileageTotal() {
        return mileageTotal;
    }

    public void setMileageTotal(double mileageTotal) {
        this.mileageTotal = mileageTotal;
    }

    public double getTotalTakeMass() {
        return totalTakeMass;
    }

    public void setTotalTakeMass(double totalTakeMass) {
        this.totalTakeMass = totalTakeMass;
    }

    public double getTotalPullMass() {
        return totalPullMass;
    }

    public void setTotalPullMass(double totalPullMass) {
        this.totalPullMass = totalPullMass;
    }

    public double getSitPeopleNum() {
        return sitPeopleNum;
    }

    public void setSitPeopleNum(double sitPeopleNum) {
        this.sitPeopleNum = sitPeopleNum;
    }

    public String getCarType() {
        return carType;
    }

    public void setCarType(String carType) {
        this.carType = carType;
    }

    public int getCarCount() {
        return carCount;
    }

    public void setCarCount(int carCount) {
        this.carCount = carCount;
    }
}
