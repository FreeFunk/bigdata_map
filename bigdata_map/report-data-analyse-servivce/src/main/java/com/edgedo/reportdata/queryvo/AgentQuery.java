package com.edgedo.reportdata.queryvo;

import com.edgedo.common.base.QueryObj;

import java.util.Date;

public class AgentQuery extends QueryObj {
    //省ID
    private String provinceId;
    //省名
    private String provinceName;
    //城市ID
    private String cityId;
    //城市名
    private String cityName;
    //县区ID
    private String xianquId;
    //县区名
    private String xianquName;
    //查询时间
    private Date searchTime;
    //查询类型（年度:YEAR,月度，MONTH）
    private String searchType;
    //开始时间
    private Date startTime;
    //结束时间
    private Date endTime;
    //学员姓名
    private String stuName;

    public String getStuName() {
        return stuName;
    }

    public void setStuName(String stuName) {
        this.stuName = stuName;
    }

    public Date getStartTime() {
        return startTime;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    public Date getEndTime() {
        return endTime;
    }

    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }

    public String getProvinceId() {
        return provinceId;
    }

    public void setProvinceId(String provinceId) {
        this.provinceId = provinceId;
    }

    public String getProvinceName() {
        return provinceName;
    }

    public void setProvinceName(String provinceName) {
        this.provinceName = provinceName;
    }

    public String getCityId() {
        return cityId;
    }

    public void setCityId(String cityId) {
        this.cityId = cityId;
    }

    public String getCityName() {
        return cityName;
    }

    public void setCityName(String cityName) {
        this.cityName = cityName;
    }

    public String getXianquId() {
        return xianquId;
    }

    public void setXianquId(String xianquId) {
        this.xianquId = xianquId;
    }

    public String getXianquName() {
        return xianquName;
    }

    public void setXianquName(String xianquName) {
        this.xianquName = xianquName;
    }

    public Date getSearchTime() {
        return searchTime;
    }

    public void setSearchTime(Date searchTime) {
        this.searchTime = searchTime;
    }

    public String getSearchType() {
        return searchType;
    }

    public void setSearchType(String searchType) {
        this.searchType = searchType;
    }

    @Override
    public String toString() {
        return "AgentQuery{" +
                "provinceId='" + provinceId + '\'' +
                ", provinceName='" + provinceName + '\'' +
                ", cityId='" + cityId + '\'' +
                ", cityName='" + cityName + '\'' +
                ", xianquId='" + xianquId + '\'' +
                ", xianquName='" + xianquName + '\'' +
                ", searchTime=" + searchTime +
                ", searchType='" + searchType + '\'' +
                ", stuName='" + stuName + '\'' +
                '}';
    }
}
