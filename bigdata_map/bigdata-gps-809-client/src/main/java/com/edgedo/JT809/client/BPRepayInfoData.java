package com.edgedo.JT809.client;

import java.util.Date;

public class BPRepayInfoData
{
  private String carcode;
  private Short carcolour;
  private boolean encrypt;
  private Date date;
  private Double lng;
  private Double lat;
  private Short speed;
  private Short drspeed;
  private Integer mileage;
  private Short direction;
  private Short altitude;
  private Integer state;
  private Integer alarm;

  public BPRepayInfoData()
  {
  }

  public BPRepayInfoData(String carcode, Short carcolour, boolean encrypt, Date date, Double lng, Double lat, Short speed, Short drspeed, Integer mileage, Short direction, Short altitude, Integer state, Integer alarm)
  {
    this.carcode = carcode;
    this.carcolour = carcolour;
    this.encrypt = encrypt;
    this.date = date;
    this.lng = lng;
    this.lat = lat;
    this.speed = speed;
    this.drspeed = drspeed;
    this.mileage = mileage;
    this.direction = direction;
    this.altitude = altitude;
    this.state = state;
    this.alarm = alarm;
  }

  public String getCarcode()
  {
    return this.carcode;
  }

  public void setCarcode(String carcode)
  {
    this.carcode = carcode;
  }

  public Short getCarcolour()
  {
    return this.carcolour;
  }

  public void setCarcolour(Short carcolour)
  {
    this.carcolour = carcolour;
  }

  public boolean isEncrypt()
  {
    return this.encrypt;
  }

  public void setEncrypt(boolean encrypt)
  {
    this.encrypt = encrypt;
  }

  public Date getDate()
  {
    return this.date;
  }

  public void setDate(Date date)
  {
    this.date = date;
  }

  public Double getLng()
  {
    return this.lng;
  }

  public void setLng(Double lng)
  {
    this.lng = lng;
  }

  public Double getLat()
  {
    return this.lat;
  }

  public void setLat(Double lat)
  {
    this.lat = lat;
  }

  public Short getSpeed()
  {
    return this.speed;
  }

  public void setSpeed(Short speed)
  {
    this.speed = speed;
  }

  public Short getDrspeed()
  {
    return this.drspeed;
  }

  public void setDrspeed(Short drspeed)
  {
    this.drspeed = drspeed;
  }

  public Integer getMileage()
  {
    return this.mileage;
  }

  public void setMileage(Integer mileage)
  {
    this.mileage = mileage;
  }

  public Short getDirection()
  {
    return this.direction;
  }

  public void setDirection(Short direction)
  {
    this.direction = direction;
  }

  public Short getAltitude()
  {
    return this.altitude;
  }

  public void setAltitude(Short altitude)
  {
    this.altitude = altitude;
  }

  public Integer getState()
  {
    return this.state;
  }

  public void setState(Integer state)
  {
    this.state = state;
  }

  public Integer getAlarm()
  {
    return this.alarm;
  }

  public void setAlarm(Integer alarm)
  {
    this.alarm = alarm;
  }
}